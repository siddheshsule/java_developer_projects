public class GalToLit {
    public static void main(String args[]){
        double gallon = 10.0; // This declares a variable for holding gallons
        double galToLitConv = 3.7854;
        double liter = gallon * galToLitConv; // This declares a variable for holding liters

        System.out.println(gallon + " gallons is " + liter + " liters");

    }
    
}
